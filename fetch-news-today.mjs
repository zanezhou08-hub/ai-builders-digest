#!/usr/bin/env node

// Daily news fetcher using Tavily API
import fs from 'fs';
import https from 'https';
import { execSync } from 'child_process';

const apiKey = process.env.TAVILY_API_KEY || 'sk-or-v1-placeholder-replace-with-real-key';
const CURRENT_DATE = new Date().toISOString().split('T')[0];

console.log('🔍 Fetching news for', CURRENT_DATE);

function fetchNews(query, category) {
    return new Promise((resolve, reject) => {
        const postData = JSON.stringify({
            query: query,
            max_results: 8,
            search_depth: 'advanced'
        });

        const options = {
            hostname: 'api.tavily.com',
            path: '/search',
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${apiKey}`,
                'Content-Length': Buffer.byteLength(postData)
            }
        };

        const req = https.request(options, (res) => {
            let data = '';
            res.on('data', (chunk) => {
                data += chunk;
            });
            res.on('end', () => {
                try {
                    const response = JSON.parse(data);
                    const newsItems = response.results || [];
                    
                    let markdown = `## ${category === 'world' ? '🌍 国际新闻' : category === 'china' ? '🇨🇳 国内新闻' : '💼 财经科技'}\n\n`;
                    
                    newsItems.forEach((item, index) => {
                        const relevance = Math.round((item.score || 0.5) * 100);
                        markdown += `- **${item.title}** - ${item.source || 'Unknown'} (relevance: ${relevance}%)\n`;
                        markdown += `  <${item.url}>\n\n`;
                    });

                    if (newsItems.length === 0) {
                        markdown += `No ${category} news available\n\n`;
                    }

                    resolve(markdown);
                } catch (error) {
                    reject(new Error('Failed to parse response: ' + error.message));
                }
            });
        });

        req.on('error', (error) => {
            reject(error);
        });

        req.write(postData);
        req.end();
    });
}

async function main() {
    try {
        console.log('Fetching world news...');
        const worldNews = await fetchNews('world news international current events', 'world');
        
        console.log('Fetching China news...');
        const chinaNews = await fetchNews('China news domestic politics economy', 'china');
        
        console.log('Fetching tech/ai news...');
        const techNews = await fetchNews('AI technology artificial intelligence startup innovation', 'tech');

        const newsFile = `${CURRENT_DATE}-news.md`;
        const worldCount = (worldNews.match(/\*\*/g) || []).length / 2;
        const chinaCount = (chinaNews.match(/\*\*/g) || []).length / 2;
        const techCount = (techNews.match(/\*\*/g) || []).length / 2;
        const totalCount = worldCount + chinaCount + techCount;
        
        const fullContent = `---
layout: default
title: "新闻速递 — ${CURRENT_DATE}"
date: ${CURRENT_DATE}
---

# 📰 新闻速递 — ${CURRENT_DATE}

---

${worldNews}
${chinaNews}
${techNews}
## 📊 今日数据

**精选文章：${totalCount} 篇**

- 🌍 国际: ${worldCount} 篇
- 🇨🇳 国内: ${chinaCount} 篇
- 💼 科技: ${techCount} 篇

---

*Generated by OpenClaw News Aggregator - ${new Date().toLocaleString('zh-CN')}*

---

## ⚠️ 数据来源

所有新闻数据均来自 Tavily API 实时搜索结果。
`;

        fs.writeFileSync(newsFile, fullContent);
        console.log('✅ News file created:', newsFile);

        // Git operations
        execSync('git add ' + newsFile);
        execSync(`git commit -m "Update news for ${CURRENT_DATE}"`);
        execSync('git push');
        console.log('✅ Changes committed and pushed');

    } catch (error) {
        console.error('❌ Error:', error.message);
        process.exit(1);
    }
}

main();