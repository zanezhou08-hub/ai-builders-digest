#!/usr/bin/env node

import fs from 'fs';
import { execSync } from 'child_process';

// Configuration
const WORK_DIR = "/Users/zx/.openclaw/workspace/follow-builders-digest";
const CURRENT_DATE = new Date().toISOString().split('T')[0];
const NEWS_FILE = `${CURRENT_DATE}-news.md`;

console.log(`📰 Starting simple news fetch for ${CURRENT_DATE}`);
process.chdir(WORK_DIR);

// Check API key
const apiKey = process.env.TAVILY_API_KEY?.trim();
if (!apiKey) {
    console.error("❌ TAVILY_API_KEY not set");
    process.exit(1);
}

console.log("✅ TAVILY_API_KEY is set");

// Function to fetch news using fetch
async function fetchNews(query, category) {
    console.log(`🔍 Fetching ${category} news...`);
    
    try {
        const response = await fetch('https://api.tavily.com/search', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${apiKey}`
            },
            body: JSON.stringify({
                query: query,
                max_results: 8
            })
        });

        if (!response.ok) {
            throw new Error(`HTTP ${response.status}: ${response.statusText}`);
        }

        const data = await response.json();
        console.log(`✅ Got ${data.results?.length || 0} ${category} articles`);
        
        let markdown = '';
        data.results?.forEach((result, index) => {
            const title = result.title || `Article ${index + 1}`;
            const url = result.url || '#';
            const source = result.source || 'Unknown';
            
            markdown += `- **${title}** - ${source}\n`;
            markdown += `  <${url}>\n\n`;
        });
        
        const tempFile = `temp-${category}.md`;
        fs.writeFileSync(tempFile, markdown);
        return true;
    } catch (error) {
        console.error(`❌ Error fetching ${category} news:`, error.message);
        fs.writeFileSync(`temp-${category}.md`, `  No ${category} news available\n\n`);
        return false;
    }
}

async function main() {
    try {
        // Fetch news
        await fetchNews("world news international breaking news", "international");
        await fetchNews("China news domestic news 中国新闻", "china");
        await fetchNews("AI technology news tech artificial intelligence startups", "tech");
        
        // Generate markdown
        let markdown = `---
layout: default
title: "新闻速递 — ${CURRENT_DATE}"
date: ${CURRENT_DATE}
---

# 📰 新闻速递 — ${CURRENT_DATE}

---

## 🌍 国际新闻

`;
        
        ['international', 'china', 'tech'].forEach(category => {
            const tempFile = `temp-${category}.md`;
            if (fs.existsSync(tempFile)) {
                const content = fs.readFileSync(tempFile, 'utf8');
                if (content.trim()) {
                    markdown += `### ${category === 'international' ? '国际' : category === 'china' ? '国内' : '科技'}新闻\n\n${content}\n`;
                }
            }
            markdown += "\n";
        });
        
        markdown += `*Generated by OpenClaw - ${new Date().toLocaleString('zh-CN')}\n\n`;
        
        // Write final file
        fs.writeFileSync(NEWS_FILE, markdown);
        console.log(`✅ News file created: ${NEWS_FILE}`);
        
        // Git operations
        try {
            execSync(`git add "${NEWS_FILE}"`);
            execSync(`git commit -m "📰 Update daily news for ${CURRENT_DATE}"`);
            execSync("git push origin master");
            console.log("✅ Changes pushed to GitHub");
        } catch (gitError) {
            console.log("⚠️ Git operations may need manual attention");
        }
        
        // Cleanup
        ['international', 'china', 'tech'].forEach(category => {
            const tempFile = `temp-${category}.md`;
            if (fs.existsSync(tempFile)) {
                fs.unlinkSync(tempFile);
            }
        });
        
        console.log(`✅ News fetch completed successfully!`);
        
    } catch (error) {
        console.error("❌ Fatal error:", error.message);
        process.exit(1);
    }
}

main();