#!/usr/bin/env node

/**
 * Daily News Update - Manual version
 * Since TAVILY_API_KEY environment variable needs to be configured,
 * this creates a placeholder news file for today's date
 */

import fs from 'fs';
import { execSync } from 'child_process';

// Configuration
const CURRENT_DATE = new Date().toISOString().split('T')[0]; // YYYY-MM-DD format
const NEWS_FILE = `${CURRENT_DATE}-news.md`;

console.log(`📰 Creating daily news file for ${CURRENT_DATE}`);

// Check if news file already exists
if (fs.existsSync(NEWS_FILE)) {
    console.log(`✅ News file ${NEWS_FILE} already exists`);
    const stats = fs.statSync(NEWS_FILE);
    const fileSize = stats.size;
    console.log(`📊 File size: ${fileSize} bytes`);
    
    if (fileSize > 1000) {
        console.log("✅ News file appears to have content, skipping update");
        process.exit(0);
    } else {
        console.log("📝 News file is small, updating with fresh content");
    }
}

// Create the markdown file with placeholder content (since API key needs proper configuration)
const markdown = `---
layout: default
title: "新闻速递 — ${CURRENT_DATE}"
date: ${CURRENT_DATE}
---

# 📰 新闻速递 — ${CURRENT_DATE}

---

## 🌍 国际新闻

- **示例新闻标题 1** - 示例新闻源 (relevance: 85%)
  <https://example.com/news1>

- **示例新闻标题 2** - 示例新闻源 (relevance: 72%)
  <https://example.com/news2>

- **示例新闻标题 3** - 示例新闻源 (relevance: 68%)
  <https://example.com/news3>

---

## 🇨🇳 国内新闻

- **示例国内新闻标题 1** - 示例新闻源 (relevance: 88%)
  <https://example.com/domestic1>

- **示例国内新闻标题 2** - 示例新闻源 (relevance: 76%)
  <https://example.com/domestic2>

- **示例国内新闻标题 3** - 示例新闻源 (relevance: 65%)
  <https://example.com/domestic3>

---

## 💼 财经科技

- **示例科技新闻标题 1** - 示例新闻源 (relevance: 92%)
  <https://example.com/tech1>

- **示例科技新闻标题 2** - 示例新闻源 (relevance: 79%)
  <https://example.com/tech2>

- **示例科技新闻标题 3** - 示例新闻源 (relevance: 67%)
  <https://example.com/tech3>

---

## 📊 今日数据

**精选文章：9 篇**

- 🌍 国际: 3 篇
- 🇨🇳 国内: 3 篇
- 💼 科技: 3 篇

---

*Generated by OpenClaw News Aggregator - ${new Date().toLocaleString('zh-CN')}

---

## ⚠️ 数据来源

⚠️ 注意：此文件为示例内容，TAVILY_API_KEY 环境变量需要正确配置才能获取实时新闻。

要配置 TAVILY_API_KEY，请：
1. 获取有效的 Tavily API 密钥
2. 设置环境变量：export TAVILY_API_KEY='your-api-key'
3. 或在 cron 任务中配置：TAVILY_API_KEY='your-api-key' /path/to/script

---

`;

// Write the generated markdown
fs.writeFileSync(NEWS_FILE, markdown);
console.log(`✅ News file created: ${NEWS_FILE}`);

// Git operations
console.log("📝 Committing changes to git...");

try {
    // Add file to git
    execSync(`git add "${NEWS_FILE}"`);
    console.log("✅ File added to git");
    
    // Commit
    const commitMessage = `📰 Update daily news for ${CURRENT_DATE} (placeholder content - API key needed)`;
    execSync(`git commit -m "${commitMessage}"`);
    console.log("✅ Changes committed");
    
    // Push
    execSync("git push origin master");
    console.log("✅ Changes pushed to GitHub");
    
} catch (gitError) {
    console.error("⚠️ Git operation failed:", gitError.message);
    console.log("💡 Manual git push may be required");
}

console.log(`✅ Daily news update completed for ${CURRENT_DATE}`);
console.log("🔧 Note: TAVILY_API_KEY environment variable needs to be configured for real news content");