#!/usr/bin/env node

/**
 * Temporary news fetcher to work around TAVILY_API_KEY issue
 * This script will fetch news from available sources and update the daily news file
 */

import fs from 'fs';
import { execSync } from 'child_process';

// Configuration
const WORK_DIR = "/Users/zx/.openclaw/workspace/follow-builders-digest";
const CURRENT_DATE = new Date().toISOString().split('T')[0]; // YYYY-MM-DD format
const NEWS_FILE = `${CURRENT_DATE}-news.md`;

console.log(`📰 Starting news update for ${CURRENT_DATE}`);

// Change to work directory
process.chdir(WORK_DIR);

// Check if TAVILY_API_KEY is set
const apiKey = process.env.TAVILY_API_KEY?.trim();
if (!apiKey) {
    console.log("⚠️ TAVILY_API_KEY not set, trying alternative approach...");
    
    // Use alternative news sources or placeholder content
    const today = new Date().toLocaleDateString('zh-CN');
    const markdown = `---
layout: default
title: "新闻速递 — ${CURRENT_DATE}"
date: ${CURRENT_DATE}
---

# 📰 新闻速递 — ${CURRENT_DATE}

---

## 🌍 国际新闻

- **Note**: Tavily API key not currently configured
- **Current time**: ${today}
- **Status**: Waiting for proper API key configuration

---

## 🇨🇳 国内新闻

- **Note**: Tavily API key not currently configured  
- **Current time**: ${today}
- **Status**: Waiting for proper API key configuration

---

## 💼 财经科技

- **Note**: Tavily API key not currently configured
- **Current time**: ${today}
- **Status**: Waiting for proper API key configuration

---

## 📊 今日数据

**精选文章：0 篇**

- 🌍 国际: 0 篇
- 🇨🇳 国内: 0 篇
- 💼 科技: 0 篇

---

*Generated by OpenClaw News Aggregator - ${new Date().toLocaleString('zh-CN')}*
- **Note**: Tavily API key not configured - please set TAVILY_API_KEY environment variable

---

## ⚠️ 数据来源

**Issue**: TAVILY_API_KEY environment variable is not set.
**Solution**: Set the environment variable:
\`\`\`
export TAVILY_API_KEY='your-api-key-here'
\`\`\`

For cron jobs, add this to your crontab:
\`\`\`
TAVILY_API_KEY='your-api-key-here' /path/to/script
\`\`\`

---

## 🔧 配置说明

To get Tavily API key:
1. Visit https://tavily.com
2. Sign up for free API key
3. Set environment variable in your shell or cron job
4. Run the news fetcher again

---

## 📋 手动更新说明

If you need to update manually until API key is configured:

1. Set TAVILY_API_KEY environment variable
2. Run: node cron-news-fetcher-tavily.mjs
3. Or use the full script directly

---

**Current file will be updated when TAVILY_API_KEY is properly configured.**
`;

    // Write the markdown file
    fs.writeFileSync(NEWS_FILE, markdown);
    console.log(`✅ News file generated with configuration note: ${NEWS_FILE}`);
    
    // Git operations
    console.log("📝 Attempting to commit configuration note...");
    
    try {
        // Add file to git
        execSync(`git add "${NEWS_FILE}"`);
        console.log("✅ File added to git");
        
        // Commit
        const commitMessage = `⚠️ Update daily news for ${CURRENT_DATE} - API key configuration needed`;
        execSync(`git commit -m "${commitMessage}"`);
        console.log("✅ Changes committed");
        
        // Push
        execSync("git push origin master");
        console.log("✅ Changes pushed to GitHub");
        
    } catch (gitError) {
        console.error("⚠️ Git operation failed:", gitError.message);
        console.log("💡 Manual git push may be required");
    }
    
    console.log("🔧 Please configure TAVILY_API_KEY environment variable for proper news fetching");
    
} else {
    console.log("✅ TAVILY_API_KEY is set, running full news fetcher...");
    
    // Import and run the main fetcher
    try {
        // Set the environment variable for subprocess
        const env = { ...process.env, TAVILY_API_KEY: apiKey };
        
        // Run the original fetcher with the API key
        execSync(`TAVILY_API_KEY="${apiKey}" node cron-news-fetcher-tavily.mjs`, {
            stdio: 'inherit',
            env: env
        });
        
    } catch (error) {
        console.error("❌ Error running main fetcher:", error.message);
        process.exit(1);
    }
}

console.log(`📰 News update process completed for ${CURRENT_DATE}`);