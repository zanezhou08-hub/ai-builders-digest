// Generate markdown content
function generateNewsMarkdown() {
  const international = newsData.international;
  const china = newsData.china;
  const tech = newsData.tech;
  
  const totalArticles = international.length + china.length + tech.length;
  
  let markdown = `---
layout: default
title: "新闻速递 — ${CURRENT_DATE}"
date: ${CURRENT_DATE}
---

# 📰 新闻速递 — ${CURRENT_DATE}

---

## 🌍 国际新闻
`;

  international.forEach(item => {
    markdown += `- **${item.title}** (relevance: ${item.relevance}%)\n`;
    markdown += `  ${item.url}\n`;
    markdown += `  ${item.content}\n\n`;
  });

  markdown += `## 🇨🇳 国内新闻\n`;

  china.forEach(item => {
    markdown += `- **${item.title}** (relevance: ${item.relevance}%)\n`;
    markdown += `  ${item.url}\n`;
    markdown += `  ${item.content}\n\n`;
  });

  markdown += `## 💼 财经科技\n`;

  tech.forEach(item => {
    markdown += `- **${item.title}** (relevance: ${item.relevance}%)\n`;
    markdown += `  ${item.url}\n`;
    markdown += `  ${item.content}\n\n`;
  });

  markdown += `## 📊 今日数据

**精选文章：${totalArticles} 篇**

- 🌍 国际: ${international.length} 篇
- 🇨🇳 国内: ${china.length} 篇
- 💼 科技: ${tech.length} 篇

---

*Generated by OpenClaw News Aggregator - ${new Date().toLocaleString('zh-CN')}*

---

## ⚠️ 数据来源

模拟数据基于当前新闻趋势，用于演示目的。

`;

  return markdown;
}

